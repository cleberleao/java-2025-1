public class OperacoesMatematicas {
    public static void main(String[] args) {

        System.out.printf("Operações Matemáticas");

        double som, sub, div, mul;

        som = 12 + 3;
        sub = 7 - 2;
        div = 2 / 2;
        mul = 2 * 3;

        System.out.println("A soma de 12 + 3 e igual: " + som);
        System.out.println("A subitração de 7 -2 é: " + sub);
        System.out.println("A divisão de 2 / 2 é = " + div);
        System.out.println("A multiplicação de 2 *3 é: " + mul);
    }
}