public class TipoVariavel {
    public static void main(String[] args) {
        int a;
        double b;
        String c;
        boolean d;

        a = 7;
        b = 5.23;
        c = "Boa noite";
        d = true;

        System.out.println("Este é meu número Inteiro: "+ a);
        System.out.println("Este é meu número  Real: "+ b);
        System.out.println("Este é meu número caractere: "+ c);
        System.out.println("Este é meu número booleado: "+ d);
    }
}
